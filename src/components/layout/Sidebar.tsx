interface SidebarProps {
    isMobile?: boolean;
    onClose?: () => void;
}

const Sidebar: React.FC<SidebarProps> = ({ isMobile, onClose }) => {
    const { t } = useTranslation();
    const location = useLocation();
    const { user, logout } = useAuthStore();
    const { canAccessSidebarItem } = usePermissions();
    
    // Фильтруем элементы сайдбара на основе разрешений пользователя
    const availableItems = sidebarItems.filter(item => canAccessSidebarItem(item.id));

    return (
        <div
            className={`bg-card-bg flex flex-col h-full overflow-y-auto ${isMobile ? 'w-64' : 'w-64 hidden md:flex'}`}
        >
            {/* User Profile Section */}
            <div className="p-4 border-b border-darkest-bg">
                <div className="flex items-center space-x-2">
                    <div className="w-10 h-10 bg-gold rounded-full flex items-center justify-center">
                        <span className="text-darkest-bg font-semibold">
                            {user?.firstname?.[0]}{user?.lastname?.[0]}
                        </span>
                    </div>
                    <div className="flex-1 min-w-0">
                        <h2 className="text-white truncate font-medium">
                            {user?.firstname} {user?.lastname}
                        </h2>
                        <p className="text-sm text-gray-300 truncate">{user?.phone}</p>
                    </div>
                </div>
            </div>

            {/* Navigation Items */}
            <nav className="flex-1 py-4">
                <ul className="space-y-1">
                    {availableItems.map((item) => {
                        const isActive = location.pathname.startsWith(item.path);
                        return (
                            <li key={item.id}>
                                <Link
                                    to={item.path}
                                    className={`flex items-center px-4 py-3 mx-2 rounded-md transition-colors group ${isActive
                                            ? 'bg-gold text-darkest-bg'
                                            : 'text-white hover:bg-darkest-bg'
                                        }`}
                                    onClick={isMobile ? onClose : undefined}
                                    title={t(`sidebar.${item.id}`)}
                                >
                                    <span className="inline-block mr-3">
                                        {item.icon}
                                    </span>
                                    <span>{t(`sidebar.${item.id}`)}</span>
                                </Link>
                            </li>
                        );
                    })}
                </ul>
            </nav>

            {/* Logout Button */}
            <div className="p-4 border-t border-darkest-bg">
                <button
                    onClick={logout}
                    className="flex items-center w-full px-4 py-2 text-white hover:bg-darkest-bg rounded-md transition-colors"
                >
                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-5 h-5 mr-3">
                        <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15M12 9l-3 3m0 0l3 3m-3-3h12.75" />
                    </svg>
                    {t('auth.logout')}
                </button>
            </div>
        </div>
    );
};

export default React.memo(Sidebar);
